let x = 4;
let y = 6;

// vamos comparar com o operador ( E = && )

// para sastifazer essa condiçao ambas tem que ser verdadeiro
console.log(x == 3 && y == 6); 
console.log(x >=3 && y >=6); // aqui obtvemos as duas partes ai DA VERDADEIRO

// vamos comparar com o operador ( OU = || )
// para satisfazer essa condiçaõ apenas uma delas seja VERDADEIRA
console.log(x == 3 || y == 6 );

// para poder da falso ambas as partes tem que ser FALSOS vamos ver um exemplo abaixo

console.log(x == 3 || y == 5);
console.log(x >= 5 || y >= 7);