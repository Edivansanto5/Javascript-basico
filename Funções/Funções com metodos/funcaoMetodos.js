// quando combinamos funções com  objetos,obtemos os metados
// todos os objetos em javascript possuem metados
// vejamos o exemplo abaixo

let a = [] // variavel a recebendo um array vazio
a.push(1,2,3,4,5) // variavel a recebendo elementos atraves do metado PUSH()
a.reverse() // invertendo os valores dos atributos com o metado REVERSE()

console.log(a); 



